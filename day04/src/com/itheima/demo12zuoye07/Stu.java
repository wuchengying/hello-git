package com.itheima.demo12zuoye07;

public class Stu {
    private int snum;
    private double score;
    public Stu() {

    }
    public Stu(int snum, double score) {
        this.snum = snum;
        this.score = score;
    }

    public int getSnum() {
        return snum;
    }

    public void setSnum(int snum) {
        this.snum = snum;
    }

    public double getScore() {
        return score;
    }

    public void setScore(double score) {
        this.score = score;
    }
}
